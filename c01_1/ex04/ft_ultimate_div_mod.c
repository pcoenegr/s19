#include <unistd.h>

void	ft_ultimate_div_mod(int *a, int *b)
{
	int swp1;
	int swp2;

	swp1 = *a;
	swp2 = *b;
	*a = swp1 / swp2;
	*b = swp1 % swp2;
}
